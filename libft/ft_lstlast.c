#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*pt;

	if (!lst)
		return (0);
	pt = lst;
	while (pt->next != NULL)
	{
		pt = pt->next;
	}
	return (pt);
}
