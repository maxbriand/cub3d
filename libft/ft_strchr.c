#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*pt;

	c %= 128;
	if (ft_isascii(c) == 0)
	{
		pt = ((char *)&s[0]);
		return (pt);
	}
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i++;
	}
	if (s[i] == c)
		return ((char *)&s[i]);
	return (0);
}
